function setLanguage(lang) {
    if (!translations[lang]) lang = 'en';
    const currentLangText = document.getElementById('currentLang');
    const langNames = { 'en': 'EN', 'tr': 'TR', 'ar': 'AR', 'id': 'ID', 'fr': 'FR' };
    if (currentLangText) currentLangText.textContent = langNames[lang];
    document.querySelectorAll('.lang-option').forEach(opt => {
        if (opt.getAttribute('data-lang') === lang) {
            opt.style.fontWeight = 'bold';
            opt.style.color = 'var(--primary)';
        } else {
            opt.style.fontWeight = 'normal';
            opt.style.color = 'var(--text-secondary)';
        }
    });
    localStorage.setItem('islamvy_lang', lang);
    document.documentElement.setAttribute('data-lang', lang);
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });

    const blogLink = document.getElementById('navBlogLink');
    if (blogLink) {
        blogLink.href = `blog/${lang}/index.html`;
    }
    if (lang === 'ar') {
        document.documentElement.dir = 'rtl';
        document.body.style.fontFamily = "'Amiri', serif";
    } else {
        document.documentElement.dir = 'ltr';
        document.body.style.fontFamily = '';
    }
}
