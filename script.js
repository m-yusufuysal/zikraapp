function shuffle(array) {
    let currentIndex = array.length, randomIndex;
    while (currentIndex !== 0) {
        randomIndex = Math.floor(Math.random() * currentIndex);
        currentIndex--;
        [array[currentIndex], array[randomIndex]] = [array[randomIndex], array[currentIndex]];
    }
    return array;
}

function renderReviews() {
    const track = document.getElementById('reviews-track');
    if (!track) return;

    track.innerHTML = '';
    const shuffled = shuffle([...reviewsPool]);

    // Triple items for seamless loop
    const itemsToRender = [...shuffled, ...shuffled, ...shuffled];

    itemsToRender.forEach((rev, idx) => {
        const card = document.createElement('div');
        card.className = `testimonial-card ${Math.random() > 0.7 ? 'highlighted' : ''}`;

        let ratingHtml = '';
        for (let i = 0; i < 5; i++) {
            ratingHtml += i < rev.rating ? '★' : '☆';
        }

        const gradientIdx = (idx % 3) + 1;

        card.innerHTML = `
            <div class="testimonial-header">
                <div class="avatar gradient-${gradientIdx}"></div>
                <div class="author-info">
                    <span class="author-name">${rev.name}</span>
                    <span class="author-location">${rev.location}</span>
                </div>
                <div class="testimonial-stars">${ratingHtml}</div>
            </div>
            <p class="testimonial-text">"${rev.text}"</p>
        `;
        track.appendChild(card);
    });
}
