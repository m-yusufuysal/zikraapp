/**
 * Get the daily quote for a given language
 * Uses day-of-year for deterministic rotation
 * @param {string} language - Language code (tr, en, ar, fr, id)
 * @returns {{ text: string, source: string }}
 */
export const getDailyQuote = (language = 'tr') => {
    const quotes = DAILY_QUOTES[language] || DAILY_QUOTES.tr;
    const now = new Date();
    const start = new Date(now.getFullYear(), 0, 0);
    const diff = now - start;
    const oneDay = 1000 * 60 * 60 * 24;
    const dayOfYear = Math.floor(diff / oneDay);
    return quotes[dayOfYear % quotes.length];
};

export default DAILY_QUOTES;
